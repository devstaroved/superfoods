    <script>
        document.addEventListener('DOMContentLoaded', function () {
            var openBtn = document.getElementById('openCompositionModal');
            var backdrop = document.getElementById('compositionModalBackdrop');
            var closeBtn = document.getElementById('compositionModalClose');
            if (openBtn) {
                openBtn.addEventListener('click', function (e) {
                    e.preventDefault();
                    backdrop.style.display = 'flex';
                    document.body.style.overflow = 'hidden';
                }, { passive: true });
            }
            if (closeBtn) {
                closeBtn.addEventListener('click', function () {
                    backdrop.style.display = 'none';
                    document.body.style.overflow = '';
                }, { passive: true });
            }
            if (backdrop) {
                backdrop.addEventListener('click', function (e) {
                    if (e.target === this) {
                        backdrop.style.display = 'none';
                        document.body.style.overflow = '';
                    }
                });
            }
            document.addEventListener('keydown', function (e) {
                if (e.key === 'Escape' && backdrop && backdrop.style.display === 'flex') {
                    backdrop.style.display = 'none';
                    document.body.style.overflow = '';
                }
            });
        });
    </script>
